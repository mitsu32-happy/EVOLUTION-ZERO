export function getStageConfig(stageId) {
  return STAGE_CONFIGS[stageId] ?? STAGE_CONFIGS.jungle;
}

export function getDifficultyConfig(difficultyId) {
  return DIFFICULTY_CONFIGS[difficultyId] ?? DIFFICULTY_CONFIGS.normal;
}

export function getDinoConfig(dinoId) {
  return DINO_CONFIGS[dinoId] ?? DINO_CONFIGS.velociraptor;
}

export function getStageGimmickConfig(stageId, difficultyId = 'normal') {
  const stage = STAGE_GIMMICK_CONFIGS[stageId] ?? STAGE_GIMMICK_CONFIGS.jungle;

  if (stage.enabled === false) {
    return stage;
  }

  return stage[difficultyId] ?? stage.normal ?? { enabled: false };
}

export function getStageBossConfig(stageId, difficultyId = 'normal') {
  const stage = STAGE_BOSS_CONFIGS[stageId] ?? STAGE_BOSS_CONFIGS.default;

  return stage[difficultyId] ?? stage.normal ?? STAGE_BOSS_CONFIGS.default.normal;
}
