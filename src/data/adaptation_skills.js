export const getAdaptationSkillById = (id) => ADAPTATION_SKILLS.find((skill) => skill.id === id) ?? null;

export const getAdaptationSkillsByTag = (tag) => ADAPTATION_SKILLS.filter((skill) => skill.tag === tag);

export const getInitialAdaptationSkills = () =>
  ADAPTATION_SKILLS.filter((skill) => skill.unlockType === 'initial');

export const getResearchUnlockAdaptationSkills = () =>
  ADAPTATION_SKILLS.filter((skill) => skill.unlockType === 'research');

export const isAdaptationSkillUnlocked = (skill, researchLevels = {}) => {
  if (!skill || skill.type !== 'adaptationSkill') {
    return true;
  }

  if (skill.unlockType !== 'research') {
    return true;
  }

  return Boolean(skill.researchId && (researchLevels?.[skill.researchId] ?? 0) > 0);
};
